package week3.day2;

public class CharCount {

	public static void main(String[] args) {
		int count = 0;
		String str = "TestLeaf";
		char[] ch = str.toCharArray();
		for (char c : ch) {
			if(c=='T') {
				count++;
			}
		}
		System.out.println("Char count of T :"+count);
		
		
		
		
		
		
		
		
		
		
		
	}
	
}
